      <p>More help is on way for families facing severe drought in Latur district. Dr Subhash Chandra, chairman of the Essel Group, has announced his decision of providing Rs4,000 per month to each of the 200 families in Gangapur village of Latur district.</p>
      <p>The humanitarian aid comes even as the government and other agencies are looking to help the people of Latur, which is facing the worst-ever drought in recent years, with water being available for drinking once in several days. The three consecutive years of drought have stopped all work, including in schools and colleges.</p>
      <p>In most cases, people have moved out of their homes to neighbouring cities. Wells have dried and up there is no work to do. Some days ago, the government imposed section 144 of the Indian Penal Code (IPC) in Latur, fearing water wars near areas where water is stored. The section prohibits assembly of five or more people in one area.</p>
      <p>In a tweet, Dr Chandra stated, "In Maharashtra, Latur District v Gangapur 200 families have no food/water. I will support them with 4000 each/month."</p>
      <p>Meanwhile, the government has also announced that water will be provided to drought-hit areas through trains. dna had reported about special rakes being readied in Rajasthan for the purpose. Each coach will have a carrying capacity of 50,000 litres of water. The water will be taken from Pandharpur to Latur. Pandharpur, which is also facing a water shortage, will get some from Ujni dam.</p>
      <p>The government is looking to supply 200 litres of water per family for 15 days. Each of the 75 tankers deployed in Latur makes at least six to seven trips, but they are still not able to provide adequate drinking water to the five lakh-odd population.</p>
