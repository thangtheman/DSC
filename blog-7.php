          <p>It is a matter of immense pride for me to share with you that Essel Group has completed 90 Glorious Years this May 2016. A family firm, by the name of M/s Ram Gopal Inder Prashad, which was formed by my great grandfather Late Ram Gopal Ji in the year 1926, was reconstituted in a family separation as M/s Subhash Chandra Laxmi Narain, the abbreviated version of which went on to become Essel. </p>

<p>Today, I take this opportunity to write to you about this journey, which I feel is remarkable because not only was it filled with challenges and difficulties, but was also treacherous enough to take us through not one, but five downturns. A journey of each downturn was faced bravely with utmost determination and a ‘never give up’ attitude of my forefathers. These attributes were imbibed by me and my brothers making us stronger with every difficulty we faced. A journey of creating history, venturing into unknown territories, only to emerge as leaders, capitalising our pioneering vision and sheer entrepreneurial spirit.</p>

<p>In its humble beginning, over 40 generations back in time, my forefathers had to migrate from Agroha to Fatehpur in search of a place to settle down and earn their living. 40 generations later, in the year 1926, my great grandfather, together with his sons, decided to move to Bhadra, a small place in Ganganagar District to enhance the business and thereafter to Okara, which is now known as Mint Gumri. To further the trading business, my grandfather Shri Jagan Nath Goenka, with his brothers, moved to Sadalpur, a small village close to Hisar, to set up a grain market in a place called Mandi Adampur. From there our eventful journey as Essel Group started - a journey from the “A” of Adampur to “Z” of ZEE!</p>

<p>Today, Essel Group is a multi-billion dollar business conglomerate, having diverse business presence across media, entertainment, real estate, packaging, infrastructure, education, technology and much more; enriching billions of lives across the world.</p>

<p>I, and my brothers, Laxmi, Jawahar and Ashok, today are extremely proud to witness the fact that Essel Group is now being efficiently managed by the fifth generation of its founders, comprising of Punit, Amit, Atul, Gaurav, Gagan, Ankit and Arpit. This probably makes us unique since most of the businesses in India have seldom lasted beyond two to three generations. As we celebrate this journey, the sixth generation is gearing up to embrace their responsibilities towards the businesses. While the rich legacy of the Group enriches us with the valuable experience, knowledge and expertise, the #Youngat90 spirit which is imbibed across each one of you gives us the fillip to touch newer global heights. </p>

<p>I must mention that this journey wouldn’t have been possible without the hard work and dedication of each one of our employees and many who are currently not a part of the Group. All employees who have participated and contributed to this journey have displayed a strong sense of ownership, starting with Lakhmi Chand Ji (Munim Ji) who was perhaps the first employee, all our colleagues of the past and each one of you in the current times. I am also certain that our current & future employees will carry forward the legacy of the Group with complete passion, dedication and above all, the spirit of #YoungAt90!
</p>


          
