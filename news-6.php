      <p><strong>Board of Directors appoint RK Arora as Executive Director and CEO of the company</strong></p>
      <p>Subhash Chandra has resigned from the post of Director and Non-Executive Chairman of Zee Media Corporation Limited, a sister company of ZEEL.</p>
<p>RK Arora, the recently appointed CEO, has been given the responsibility of Additional Director, thereby appointing him as Executive Director and CEO of the company.
</p>
<p>As per the BSE filing by Zee Media Corporation, the decision was taken at a Board of Directors meeting held on May 24, 2016.</p>
<p>The filing read: “…took on record the resignation of Dr Subhash Chandra as Director and Non-Executive Chairman of the Company with effect from May 24, 2016; and has, based on recommendations of Nomination and Remuneration Committee, approved appointment of R K Arora, CEO as an Additional Director and upon such appointment, his appointment as an Executive Director and CEO of the Company for a period of 3 years with effect from May 24, 2016.”</p>

<p>ZMCL is an Essel Group Company. Chandra is the Chairman, Essel Group and ZEE, having founded the company in 1990s. He is known as a media moghul for initiating the private television era in India among many other firsts.</p>

<p>Zee Media Corporation Limited (formerly called Zee News Ltd) has 10 news channels in six different languages reaching nearly 119 million viewers. It comprises the Mumbai edition of DNA newspaper and digital properties such as zeenews.com and dnaindia.com reaching more than 200 million users.</p>

<p>ZMCL comprises three national channels Zee News (Hindi News), India 24X7, and Zee Business (Hindi Business News) and other regional ones, including Zee 24 Taas (Marathi), Zee Madhya Pradesh Chhattisgarh (Madhya Pradesh and Chhattisgarh), Zee Marudhara (Rajasthan), Zee Punjab Haryana Himachal and Zee Kalinga (Odisha). Additionally the company operates two regional news channels through its subsidiaries — 24 Ghanta (Bengali) through Zee Akaash News and Maurya TV (Bihar and Jharkhand) through Maurya TV.</p>


