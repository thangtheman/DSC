      <p>Come Wednesday, Mumbai will share a place in history witnessing The Battle for Sanskrit at the IIT Bombay Convocation Hall at 6.30pm.</p>
      <p>Rajiv Malhotra, an Indian-American thinker, ex-entrepreneur and the author of several national best-sellers will be delivering a talk on "Geopolitics and the Study of Indian Civilization". Renowned Sanskrit scholar of the Institute Prof Krishnamurthy Ramasubrianian will host the event. Zee and Essel group chairman Dr Subhash Chandra, whose autobiography "The Z-factor" was recently released by PM Narendra Modi, will be gracing the event as the chief guest. The talk and discussion will be followed by an award winning one-act Sanskrit play.</p>
      <p>Special address by Dr Subhash Chandra and an introductory note by Prof Ramasubranian will underline the importance of this "battle" at this critical juncture of self-discovery of the idea of India. Pre-release, personally autographed copies of the book "The Battle for Sanskrit" along with other relevant books will be available for sale at the venue.</p>
      <p>This talk will be about the authority and institutional mechanisms for studying the Indian civilization which are controlled by the West and by the Indians who are trained in the West. This is different from the study of China, Japan, Arab, Persian, Russian, etc. cultures which are controlled by their respective nations today. After India's independence this situation has become worse. This lecture discusses the reasons and consequences for this situation. It proposes solutions in order to transition from videshi to swadeshi academic studies of India. All those who are concerned about our heritage would benefit from these insights.</p>
