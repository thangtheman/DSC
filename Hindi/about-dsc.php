         <h3>देश के लोगों के नाम डॉ. सुभाष चंद्रा का संदेश </h3>
          <br/>
          <p>हमारे देश में एक वैश्विक सुपर पॉवर बनने की ज़बर्दस्त क्षमता है और देश के युवा इस दिशा में अपना महत्वपूर्ण योगदान दे सकते हैं। </p>

<p>मुझे अपने भारतीय होने पर गर्व है, और मैं अपने राष्ट्र की कल्पना को कामयाब करने के लिए सभी बाधाओं और चुनौतियों सामना करने में सक्षम हूँ। देश के एक नागरिक के रूप में, ये मेरा कर्तव्य और ज़िम्मेदारी है कि मैं रहे मेरे देश के नागरिकों के समक्ष मौजूद उन चुनौतियों का सामना करने और उनके उन्मूलन की दिशा में काम करुँ, जो हमारे देश को विकसित होने व वैश्विक मंच पर अपनी पहचान बनाने में बाधा पैदा कर रही है। </p>

<p>मैं अपने देश के युवाओं को हमेशा यही सलाह देता हूँ कि अगर वे पूरी ईमानदारी से अपना काम करे तो उनकी क्षमता में वृध्दि होने के साथ ही सफलता हासिल करने के मौके भी ज्यादा मिलेंगे। पूरी ईमानदारी और निष्ठा से मैं ये ज़िम्मेदारी लेता हूँ कि आज के युवाओं को बेहतर कल के लिए तैयार करुंगा। और मेरा दृढ़ विश्वास है कि यह समय अब आ गया है। अब समय आ गया है कि  राष्ट्र के विकास में बाधा पैदा करने वाले भ्रष्टाचार और फर्ज़ीवाड़े को समूल नष्ट करना होगा। </p>

<p>अब समय आ गया है कि देश की राजनीति को एक नए सिरे से परिभाषित किया जाए। अब समय आ गया है कि सार्वजनिक सेवा की ओर हमारा ध्यान जाए। और यह सब तभी संभव है, जब हम सब आज ही इसके लिए जुट जाएँ। </p>
<div class="additional-info" id="t2">
<p>मुझे ये देखकर बेहद तकलीफ़ होती है कि हमारे देश की युवा पीढ़ी आत्मविश्वास और की संकल्प शक्ति की कमी की वजह से अवसरों को  खोती जा जा रही है। मुझे ये देखकर दुःख होता है कि देश में बड़ी संख्या ऐसे लोगों की है जिन्हें बुनियादी सुविधाएँ तक उपलब्ध नहीं है। देश के, खासकर ग्रामीण क्षेत्रों में रहने वाले युवाओं को हम देश के विकास में भागीदार नहीं बना पा रहे हैं। देखने में ये आ रहा है कि हमारे देश के लोगों में आत्मविश्वास, दृढ़ निश्चय और इन सबसे ऊपर देश के लिए कुछ कर गुज़रने की भावना की कमी है। किसी भी सबसे महत्वपूर्ण बात ये है कि हम  सब मिलकर इन तमाम बाधाओं का मुकाबला करें। आज युवाओं को इस बात का एहसास कराया जाना चाहिए कि उनके अंदर ज़बर्दस्त क्षमता है और वे किसी भी चुनौती का सामना कर सकते हैं। अगर आपके अंदर संकल्प शक्ति है, और खुद पर भरोसा है, और खुद के अपने सपने हैं; तो फिर दुनिया की कोई ताकत आपका रास्ता नहीं रोक सकती। </p>

<p>सबसे पहले देश के एक जिम्मेदार नागरिक के रुप में और फिर एक मार्गदर्शक के रूप में मैने ये संकल्प लिया है कि मैं समाज में खासकर युवाओं में कुछ बदलाव ला सकूँ। मेरा पक्का यक़ीन है कि मेरा अनुभव, विशेषज्ञता और मेरे द्वारा हासिल ज्ञान से मुझमें ये क्षमता है कि मैं अपनी बात असरकारी तरीके से युवाओं तक पहुँचा सकता हूँ। </p>

<p>मैने आज तक जो भी कुछ पाया है, अब मैं अपना अधिकांश समय समाज के सामाजिक –आर्थिक उत्थान के लिए देना चाहता हूँ। लेकिन, मैं अकेला ये सब नहीं कर सकता, और इसके लिए मुझे आपका सहयोग तो चाहिए ही इसके साथ ही आपकी सक्रियता और भागीदारी भी चाहिए। इस संदेश के माध्यम से मैं अपने देश के सभी युवाओँ से अपील करता हूँ कि वे जागें और अपने अधिकारों और ज़िम्मेदारियों के प्रति सजग बनें। प्रोद्योगिकी  के आगमन के साथ ही इस वेब साईट जैसे डिजिटल माध्यम या सोशल मीडिया के मंच से  हम अपने विचारों और सोच को ज्यादा से ज्यादा लोगों तक पहुँचा सकते हैं, और हम अपने देश को एक वैश्विक सुपर पॉवर बना सकते हैं। हम सबको मिलकर देश को आगे लाने की दिशा में सोचना होगा। मैं “वसुधैव कुटुंबकम“ में विश्वास रखता हूँ, यानी पूरा विश्व ही एक परिवार है। और इसी वजह से मैं इस बात में विश्वास रखता हूँ कि हम सब एक ही परिवार के लोग हैं, और हम सबको मिलकर अपने देश के विकास के लिए काम करना होगा। हमारे परिवार को हमारी ज़रुरत है। हमारे राष्ट्र को हमारी ज़रुरत है। </p>
          <p>हमें सबसे पहले अपने देश की असल समस्याओं को समझना होगा, और इनका समाधान निकालने के साथ ही इन्हें खत्म भी करना होगा। आप मुझे पर लिखें और एक नए भारत की यात्रा शुरु करें। </p>
