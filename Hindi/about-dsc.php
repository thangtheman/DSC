          <br/>
          <p> <b>डॉ. सुभाष चंद्रा का नाम तीन साधारण शब्दों में परिभाषित किया जा सकता है</b></p>

<p><b>दूरदृष्टा,  परोपकारी और मार्गदर्शक</b></p>

<p>एक ऐसा मार्गदर्शक,  जिसने अपनी क्षमता का उपयोग राष्ट्र को सशक्त बनाने में किया  और इसे एक वैश्विक पहचान दी। एक ऐसा योध्दा जिसने अपने सामने आने वाले हर संकट का डटकर सामना किया और राष्ट्र के सामने मौजूद समस्याओँ का भी निराकरण किया। एक ऐसा दूरदृष्टा जिसने <b>90</b> के दशक में भारत की उदारीकरण की नीतियों में अपना योगदान देते हुए देश का पहला निजी चैनल ही शुरु नहीं किया बल्कि एक पूरे उद्योग के लिए मार्ग प्रशस्त किया और आज इस उद्योग में लाखों लोगों को रोज़गार मिल रहा है।</p>

<p>डॉ. सुभाष चंद्रा के बुध्दि चातुर्य, संकल्प और दूरदृष्टि में देश के हर व्यक्ति में एक आत्मविश्वास पैदा करने की,  हमारे देश की सोच में बदलाव लाने और देश में आवश्यक परिवर्तन लाने की क्षमता है।</p>
<p>डॉ. सुभाष चंद्रा ने अपने दम पर ही अपनी पहचान बनाते हुए हर बार नए उद्यम की पहचान कर उसे सफल कारोबार में बदलकर दिखाया है। वे अपने संपन्न अनुभव को को <b>डीएससी शो</b> के माध्यम से देश भर के  <b>230</b> मिलियन दर्शकों तक पहुँचाते हैं। अपने इस लोकप्रिय कार्यक्रम के माध्यम से एक उर्जावान मार्गदर्शक के रूप में वे युवाओं के जीवन को एक नई दिशा देने के साथ ही उनमें कुछ कर गुज़रने का आत्मविश्वास भी पैदा कर रहे हैं।  </p>


<div class="additional-info" id="t1">
    <p>डॉ. सुभाष चंद्रा ने डीएससी फाउंडेशन की छतरी चले समाज के लिए कई परोपकारी योजनाएँ शुरु की है। उन्होंने तालीम TALEEM (Transnational Alternate Learning for Emancipation and Empowerment through Multimedia) नाम से एक संस्था की स्थापना की है जिसके माध्यम से दूरस्थ क्षेत्रों के बच्चों को गुणवत्तापूर्ण शिक्षा दी जाती है।</p>
    <p>वे एकल विद्यालय फाउंडेशन ऑफ इंडिया के अध्यक्ष भी हैं-जिसके माध्यम से देश के वनवासी क्षेत्रों में और ग्रामीण अंचलों में विद्यालयों का संचालन किया जाता है। एकल विद्यालयों के माध्यम से लाखों आदिवासी बच्चों को शिक्षा दी जाती है। ग्लोबल विपश्यना फाउंडेशन की स्थापना में भी उनकी महत्वपूर्ण भूमिका रही है, जिसके माध्यम से लोगों को अपनी अध्यात्मिक क्षमता के विकास की दिशा में मार्गदर्शन दिया जाता है। डॉ. सुभाष चंद्रा ग्लोबल फाउंडेशन फॉर सिविल हार्मोनी इंडिया (GFCH) के भी संस्थापक अध्यक्ष हैं,  यह यूनाईडेट नेशंस एलायंस ऑफ सिविलाईज़ेशन (UNAOC) के साथ मिलकर अंतर्राष्ट्रीय विवादों को दूर करने की दिशा में कार्य करता है।  GFCH  संस्थापकों में हिज़ होलीनेस दलाई लामा सहित कई जानी मानी शख्सियतें शामिल हैं, इसके संस्थापकों में भारत के पूर्व राष्ट्रपति  स्व. डॉ. अब्दुल कलाम भी शामिल थे।  </p>

    <p>मनोरंजन और मीडिया के क्षेत्र में इस अनुपम योगदान के लिए डॉमें चंद्रा को न्यू यॉर्क   <b>39</b> वीं इंटरनेशनल एम्मी अवॉर्ड्स नाईट में एम्मी डायरेक्टोरेट अवॉर्ड प्रदान किया गया और यूनिवर्सिटी ऑप ईस्ट लंदन ने उन्हें डॉक्टोरेट ऑफ बिज़नेस एडमिनिस्ट्रेशन सम्मानित किया है।  </p>
