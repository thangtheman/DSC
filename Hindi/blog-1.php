          <p>लोग प्रायः मुझसे मेरी सफलता का रहस्य जानना चाहते हैं; और मैं उनसे विनम्रतापूर्वक मेरे दादाजी से मिली सीख के बारे में बताता हूँ कि ये मेरी सबसे बड़ी पूँजी है। उन्होंने मुझे हमेशा सच्चाई के रास्ते पर जाने की प्रेरणा दी; ये सिखाया कि अपने लक्ष्य को कैसे हासिल किया जाता है; और इसके साथ ही मुझमें ऐसा जोश पैदा किया कि मैं कभी भी हार नहीं मानूँ।</p>

<p>इस अवार्ड समारोह में मुझे माननीय कैथलीन वायनी, जो ओंटेरियो प्रोविंस के प्रमुख हैं और  वहाँ के इन्नोवेशन, विज्ञान और आर्थिक विकास मंत्री माननीय नवदीप बैंस  जैसी हस्तियों से भी मिलने मौका भी मिला। इस मौके पर मैने उनके साथ कई उपयोगी मुद्दों पर चर्चा की और मुझे उम्मीद है कि इससे हमारा ज़ी परिवार और इंडो –कैनेडियन समुदाय आगे आकर एकल विद्यालय अभियान को एक नई ऊँचाई प्रदान करेंगे।
</p>
