          <p>सीआईएफ चंचलानी ग्लोबल  इंडियन अवार्ड प्राप्त करते हुए ये अपने आप में  निश्चित ही गर्व और सम्मान की बात होती है जब हम अपने आनंद के क्षणों को किसी के साथ साझा करते हैं। आज, मैं आपके साथ अपने ज़ी परिवार से जुड़ी कुछ अच्छी खबरें बताना चाहता हूँ। ये मेरे लिए गर्व की बात है  कि मुझे सीआईएफ चंचलानी अवार्ड 2016 से सम्मानित किया जा रहा है। मैं कनाडा इंडियन फाउंडेशन (सीआईएफ) से ये विशेष ग्लोबल इंडियन अवार्ड पाकर अपने आपको सम्मानित महसूस कर रहा हँ। ये अवार्ड ज़ी परिवार द्वारा वैश्विक स्तर पर संकल्प व कठोर परिश्रम से किए जा रहे काम का परिणाम है। जब मुझे इस अवार्ड के बारे में बताया गया, तो मैं इसको लेकर दो कारणों से उत्साहित था, एक तो इतने प्रतिष्ठित अवार्ड के लिए मुझे नामित किया गया था और दूसरा, इतने प्रतिष्ठित मंच पर मैं अपने देश का प्रतिनिधित्व कर रहा था। इस पर लिखा गया ‘इंडियन’ शब्द इस मौके पर मेरे गर्व और आनंद को एक नई ऊँचाई प्रदान कर रहा था।</p>
