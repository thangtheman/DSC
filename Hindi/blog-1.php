          <p>कनाडा में इस अवार्ड के साथ ही मुझे 50,000 डॉलर की नकद राशि मिली, वह मैने एकल विद्यालय को दे दी। मुझे कनाडा में एकल विद्यालय के लिए काम करने वाले परिश्रमी और समर्पित कार्यकर्ताओं से मिलने का भी मौका मिला, उनसे मिलना मेरे लिए बेहद उपयोगी रहा।</p>

<p>मेरा मानना है कि एकल विद्यालय, एक ऐसा अभियान है जो हमारे देश का चेहरा बदल सकता है। इसके माध्यम से ग्रामीण क्षेत्रों के लोगों को आत्म निर्भर बनाकर हमारे अतीत के गौरव को पुनर्जीवित किया जा रहा है। मेरा मानना है कि एकल के माध्यम से ग्रामीण भारत के लोगों को अपने पैरों पर खड़े की एक मजबूत बुनियाद तैयार हो रही है।</p>

