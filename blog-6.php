          <p>“A Dalit woman in a remote village in India…” This is how I started narrating a story in one of the DSC shows recently. A young man in the crowd immediately stopped me and asked, “Sir, why does your story start with a Dalit woman and not just a woman. Why did you attach her caste to her identity?”</p>

<p>For 10 seconds, I was speechless absorbing what this young man said. Realizing the depth of his question, I instantly corrected myself recommencing my narration. The fearlessness with which the man spoke restored my faith in my society and especially, the youth.</p>

<p>In the past few years, I have had some interesting conversations with the young tech savvy generation of our country where their thinking has stunned me and changed me as well at certain levels.</p>

<p>I meet these young people who are so well read that they can strike up conversations on every topic from Indian political scenarios to renaissance to discussing philosophy and political thinkers to debates on socialism versus capitalism etc. One of the most surprising aspects for me is the inclination of today’s young mind towards Indian mythology. Lately, I was watching one of the talks on mythology held at the Jaipur Literature festival. The questions asked by people (mostly in the age group of 25 to 30 years) could be asked only by someone with a deep interest in the subject. And here I thought that none of the young crowd would be interested in this subject area! I’m proud to say that I’m happy to be proven wrong.  </p>

<p>The rawness, earthiness, constant fight to break the mold and moving out of defined boxes are what signify India’s youth today.</p>

<p>Unfortunately, there is another set of young people who are aimless, unskilled and disillusioned with the world, society and the country. They are constantly complaining about only the wrongs and blame everyone around them for their lack of opportunities in life. This other set is like a ticking time bomb, which if exploded can create havoc in the country, as we witnessed in recent past. It is important to harness their potential and infuse dignity in them about their existence. </p>

<p>With my resources, I try to help as many as I can though I can not reach all of them. Hence, I hope that one set of youth understands the other and helps in uplifting him or her.</p>



