      <p><strong>New Delhi:</strong> Prime Minister Narendra Modi on Wednesday launched the autobiography of Essel Group Chairman Dr Subhash Chandra.
        Modi unveiled the book at his official residence 7 RCR in Delhi. </p>
      <p>Dr Chandra, during the launch event, said that focus and determination is the key to achieving success in life.</p>
      <p>PM Modi, praising Dr Chandra, said, “It takes a lot of effort to write an autobiography. Taking risk is Subhash Chandra's habit”.</p>
      <p>Modi also praised Dr Chandra for his contribution to society and education.</p>
      <p> Applauding Dr Chandra's continuous efforts to achieve excellence, Modi said that it is very important to read biography. “It helps you know the history better,” the PM said.</p>
      <p>The autobiography titled 'The Z Factor: My Journey as the Wrong Man at the Right Time' has been written along with Pranjal Sharma. </p>
      <p>A book launch event is also scheduled at Zee Jaipur Lit Fest at 12.25 pm tomorrow.</p>
      <p>The panelists for the event are M J Akbar and Vallabh Bhansali who will interact with Dr Chandra in the launch session.</p>
      <p>The book is published by Harper Collins and will be available in stores from tomorrow onwards. It is already available on Flipkart and Amazon India for pre-orders.</p>
      <p>Dr Chandra, who is referred to as the Media Moghul of India, revolutionised the television industry by launching the country's first satellite Hindi channel Zee TV in 1992 and later the first private news channel, Zee News.</p>
      <p>His bouquet of businesses includes television networks (ZEE), a newspaper chain (DNA), cable systems (Wire and Wireless Ltd), direct-to-home (Dish TV), satellite communications (Agrani and Procall), theme parks (EsselWorld and Water Kingdom), online gaming (Playwin), education (Zee Learn), flexible packaging (Essel Propack), infrastructure development (Essel Infraprojects Ltd) and family entertainment centres (Fun Cinemas). </p>
      <p> He has made his mark as an influential philanthropist in the country by setting up TALEEM (Transnational Alternate Learning for Emancipation and Empowerment through Multimedia), aimed at providing access to quality education through distance and open learning. Dr Chandra is also the Chairman of the Ekal Vidyalaya Foundation of India – a movement to eradicate illiteracy from rural and tribal India. The Foundation provides free education to nearly eight lakh tribal children across 27,000 villages through one-teacher schools. </p>
